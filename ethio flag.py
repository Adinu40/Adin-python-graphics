from turtle import *
hideturtle()
speed(15)
width(5)
bgcolor('blue')

def draw_rectangle(linecolor):
	color(linecolor)
	begin_fill()
	right(0)
	forward(800)
	left(90)
	forward(200)
	left(90)
	forward(800)
	left(90)
	forward(200)
	end_fill()
penup()
setheading(0)
goto(-300,0)
pendown()
draw_rectangle('red')
penup()
setheading(0)
goto(-300,200)
pendown()
draw_rectangle('yellow')
penup()
setheading(0)
goto(-300,400)
pendown()
draw_rectangle('green')
penup()
goto(-50,300)
pendown()
